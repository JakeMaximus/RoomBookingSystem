    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        String custname = CustNames.getText();
        String passport = IPass.getText();
        String pnumber = Number.getText();
        String mail = Mail.getText();
        
        if(custname.length()<4){
            JOptionPane.showMessageDialog(null, "Username too short");
            CustNames.setText("");
        }else if(custname.length()>12){
            JOptionPane.showMessageDialog(null, "Username too long");
            CustNames.setText("");
        } else if(!custname.matches("[a-zA-Z]+")){
            JOptionPane.showMessageDialog(null, "Only user lower or uppercase alphabets");
            CustNames.setText("");
        } else if(passport.length() != 12 || !passport.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid IC-number");
            IPass.setText("");
        } else if(pnumber.length() != 10){
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number");
            Number.setText("");
        } else if(!mail.contains(".com")&&!mail.contains("@")){
            JOptionPane.showMessageDialog(null, "Please enter a valid email");
            Mail.setText("");
        } else{
            String View = Viewver.getSelectedItem().toString();
            String Room = rooomcombo.getSelectedItem().toString();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(CheckInDates.getDate());
            String CODate = CheckOutDates.getText();
            String NumberOfDays = NOD.getSelectedItem().toString();
            
            BookingFacts bd = new BookingFacts(View, Room, date, CODate, NumberOfDays, custname, passport, pnumber, mail);
            modifyCustDeet();
            new MainPage().setVisible(true);
            this.dispose(); 
        }
        


        
        
    }//GEN-LAST:event_SubmitActionPerformed

