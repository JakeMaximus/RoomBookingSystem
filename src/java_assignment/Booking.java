    public void modifyCustDeet(){
        String View = BookingFacts.getView();
        String Roomid = BookingFacts.getRoom();
        String CheckIn = BookingFacts.getCheckIn();
        String CheckOut = BookingFacts.getCheckOut();
        String NoDays = BookingFacts.getNOD();
        
        String Name = BookingFacts.getCustName();
        
        String IC = BookingFacts.getICNum();
        //String TotalA = Amo.getText();
        String pnumber = BookingFacts.getpnumber();
        String mail = BookingFacts.getmail();
        String details = Roomid + "," + "Available" + "," + "0" + "," + "0" + "," + "0" + "," + "0" + "," + "0" + "," + "0" + "," + "0" + "," + "0";
        String mod = Roomid + "," + "Rented" + "," + CheckIn + "," + CheckOut + "," + NoDays + "," + Name + "," + IC + "," + "RM100" + "," + pnumber + "," + mail;
        String file;
        String i = "";
        
        if(View.equals("Sea")){
            file = "SeaView.txt";
        } else{
            file = "JungleView.txt";
        }
        try {
            BufferedReader r = new BufferedReader(new FileReader(file));
            String l = r.readLine();
            while(l != null){
                i = i + l + System.lineSeparator();
                l = r.readLine();
            }
            String newC = i.replaceAll(details, mod);
            FileWriter w = new FileWriter(file);
            
            w.write(newC);
            w.close();
            r.close();
            
        }
        catch(IOException e){
            e.printStackTrace();
        }
    }
    
